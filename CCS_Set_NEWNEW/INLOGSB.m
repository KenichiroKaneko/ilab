%  ***********************************************************************
%  ++++ SUBROUTINE (No.5)  INLOGSB +++++++++++++++++++++++++++++++++++++++++++
%     特異性のある部分を対数Gauss積分公式を用いて計算
%  ***********************************************************************
%
function [GWLA, HWLA] = INLOGSB(XS, XG1, YG1, XG2, YG2, XG3, YG3, NODO)
    GWLA = zeros(1, 3);
    HWLA = zeros(1, 3);
    %//// 通常のGauss積分用のデータ ////////////////////////////////////////
    GI = [+0.9894009349D0, -0.9894009349D0, +0.9445750230D0, ...
                -0.9445750230D0, +0.8656312023D0, -0.8656312023D0, ...
                +0.7554044083D0, -0.7554044083D0, +0.6178762444D0, ...
                -0.6178762444D0, +0.4580167776D0, -0.4580167776D0, ...
                +0.2816035507D0, -0.2816035507D0, +0.0950125098D0, ...
                -0.0950125098D0];
    %
    OME = [+0.0271524594D0, +0.0271524594D0, +0.0622535239D0, ...
                +0.0622535239D0, +0.0951585116D0, +0.0951585116D0, ...
                +0.1246289712D0, +0.1246289712D0, +0.1495959888D0, ...
                +0.1495959888D0, +0.1691565193D0, +0.1691565193D0, ...
                +0.1826034150D0, +0.1826034150D0, +0.1894506104D0, ...
                +0.1894506104D0];
    %//// 対数Gauss積分用のデータ //////////////////////////////////////////
    GIL = [0.0038978344D0, 0.0230289456D0, 0.0582803983D0, ...
                0.1086783650D0, 0.1726094549D0, 0.2479370544D0, ...
                0.3320945491D0, 0.4221839105D0, 0.5150824733D0, ...
                0.6075561204D0, 0.6963756532D0, 0.7784325658D0, ...
                0.8508502697D0, 0.9110868572D0, 0.9570255717D0, ...
                0.9870478002D0];
    %
    OMEL = [0.0607917100D0, 0.1029156775D0, 0.1223556620D0, ...
                0.1275692469D0, 0.1230135746D0, 0.1118472448D0, ...
                0.0965963851D0, 0.0793566643D0, 0.0618504945D0, ...
                0.0454352465D0, 0.0310989747D0, 0.0194597659D0, ...
                0.0107762549D0, 0.0049725428D0, 0.0016782011D0, ...
                0.0002823537D0];
    %-----------------------------------------------------------------------
    PI2 = 2.D0 * pi;
    %-----------------------------------------------------------------------
    %    ! JACOBIANの中の係数
    %
    A = XG3 - 2.D0 .* XG2 + XG1;
    B = 0.5D0 .* (XG3 - XG1);
    C = YG3 - 2.D0 .* YG2 + YG1;
    D = 0.5D0 .* (YG3 - YG1);
    %
    ZETAA11 = zeros(1, 16);
    ZETAA12 = zeros(1, 16);
    ZETAA13 = zeros(1, 16);
    ZETAA21 = zeros(1, 16);
    ZETAA22 = zeros(1, 16);
    ZETAA23 = zeros(1, 16);
    ZETAB11 = zeros(1, 16);
    ZETAB12 = zeros(1, 16);
    ZETAB13 = zeros(1, 16);
    ZETAB21 = zeros(1, 16);
    ZETAB22 = zeros(1, 16);
    ZETAB23 = zeros(1, 16);
    XIA1 = zeros(1, 16);
    XJAA1 = zeros(1, 16);
    A11 = zeros(1, 16);
    A12 = zeros(1, 16);
    A13 = zeros(1, 16);
    XIA2 = zeros(1, 16);
    XJAA2 = zeros(1, 16);
    RGAX1 = zeros(1, 16);
    RGAY1 = zeros(1, 16);
    RGAX2 = zeros(1, 16);
    RGAY2 = zeros(1, 16);
    RGA = zeros(1, 16);
    XLOA = zeros(1, 16);
    A21 = zeros(1, 16);
    A22 = zeros(1, 16);
    A23 = zeros(1, 16);
    XIB1 = zeros(1, 16);
    XJAB1 = zeros(1, 16);
    B11 = zeros(1, 16);
    B12 = zeros(1, 16);
    B13 = zeros(1, 16);
    XIB2 = zeros(1, 16);
    XJAB2 = zeros(1, 16);
    RGBX1 = zeros(1, 16);
    RGBY1 = zeros(1, 16);
    RGBX2 = zeros(1, 16);
    RGBY2 = zeros(1, 16);
    RGB = zeros(1, 16);
    XLOB = zeros(1, 16);
    B21 = zeros(1, 16);
    B22 = zeros(1, 16);
    B23 = zeros(1, 16);
    EX = zeros(1, 16);
    EY = zeros(1, 16);
    XJA = zeros(1, 16);
    RNOR = zeros(1, 16);
    S1 = zeros(1, 16);
    S2 = zeros(1, 16);
    S3 = zeros(1, 16);
    %-----------------------------------------------------------------------
    %    ! 積分計算のループ
    %
    %-----------------------------------------------------------------------
    %        ! ξs(ソース点の位置)が-2/3,0,+2/3の場合に分けて計算
    %
    if (NODO == 1)
        %            ! ξs(ソース点の位置)が-2/3の場合
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! ξs(ソース点の位置)が-2/3の場合に用いる内挿関数
        %
        ZETAA11(1:16) = (5.D0 .* GIL(1:16) - 2) .* (5.D0 .* GIL(1:16) - 4) ./ 8.D0;
        ZETAA12(1:16) = -(5.D0 .* GIL(1:16) - 4) .* 5.D0 .* GIL(1:16) ./ 4.D0;
        ZETAA13(1:16) = (5.D0 .* GIL(1:16) - 2) .* 5.D0 .* GIL(1:16) ./ 8.D0;
        %
        ZETAA21(1:16) = (5.D0 .* GI(1:16) + 1) .* (5.D0 .* GI(1:16) - 3) ./ 32.D0;
        ZETAA22(1:16) = -(5.D0 .* GI(1:16) - 3) .* (5.D0 .* GI(1:16) + 5) ./ 16.D0;
        ZETAA23(1:16) = (5.D0 .* GI(1:16) + 1) .* (5.D0 .* GI(1:16) + 5) ./ 32.D0;
        %///////////////////////////////////////////////////////////////////////
        ZETAB11(1:16) = (GIL(1:16) + 2) .* (GIL(1:16) + 4) ./ 8.D0;
        ZETAB12(1:16) = -(GIL(1:16) + 4) .* GIL(1:16) ./ 4.D0;
        ZETAB13(1:16) = (GIL(1:16) + 2) .* GIL(1:16) ./ 8.D0;
        %
        ZETAB21(1:16) = (GI(1:16) - 5) .* (GI(1:16) - 9) ./ 32.D0;
        ZETAB22(1:16) = -(GI(1:16) - 9) .* (GI(1:16) - 1) ./ 16.D0;
        ZETAB23(1:16) = (GI(1:16) - 5) .* (GI(1:16) - 1) ./ 32.D0;
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[-2/3 , +1]のとき
        %
        XIA1(1:16) = (5.D0 .* GIL(1:16) - 2) ./ 3.D0;
        XJAA1(1:16) = 5.D0 .* sqrt((A .* XIA1(1:16) + B).^2 + (C .* XIA1(1:16) + D).^2) ./ 3.D0;
        %
        A11(1:16) = ZETAA11(1:16) .* XJAA1(1:16) .* OMEL(1:16);
        A12(1:16) = ZETAA12(1:16) .* XJAA1(1:16) .* OMEL(1:16);
        A13(1:16) = ZETAA13(1:16) .* XJAA1(1:16) .* OMEL(1:16);
        %///////////////////////////////////////////////////////////////////////
        XIA2(1:16) = (5.D0 .* GI(1:16) + 1) ./ 6.D0;
        XJAA2(1:16) = 5.D0 .* sqrt((A .* XIA2 + B).^2 + (C .* XIA2 + D).^2) ./ 6.D0;
        %
        RGAX1(1:16) = 0.5D0 .* (XG3 - 2.D0 .* XG2 + XG1);
        RGAY1(1:16) = 0.5D0 .* (YG3 - 2.D0 .* YG2 + YG1);
        RGAX2(1:16) = (XG3 + 4.D0 .* XG2 - 5.D0 .* XG1) ./ 6.D0;
        RGAY2(1:16) = (YG3 + 4.D0 .* YG2 - 5.D0 .* YG1) ./ 6.D0;
        RGA(1:16) = sqrt((RGAX1(1:16) .* XIA2(1:16) + RGAX2(1:16)).^2 + (RGAY1(1:16) .* XIA2(1:16) + RGAY2(1:16)).^2);
        XLOA(1:16) = log(0.6D0 ./ RGA(1:16));
        %
        A21(1:16) = ZETAA21(1:16) .* XJAA2(1:16) .* XLOA(1:16) .* OME(1:16);
        A22(1:16) = ZETAA22(1:16) .* XJAA2(1:16) .* XLOA(1:16) .* OME(1:16);
        A23(1:16) = ZETAA23(1:16) .* XJAA2(1:16) .* XLOA(1:16) .* OME(1:16);
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[-1 , -2/3]のとき
        %
        XIB1(1:16) = -(GIL(1:16) + 2) ./ 3.D0;
        XJAB1(1:16) = sqrt((A .* XIB1(1:16) + B).^2 + (C .* XIB1(1:16) + D).^2) ./ 3.D0;
        %
        B11(1:16) = ZETAB11(1:16) .* XJAB1(1:16) .* OMEL(1:16);
        B12(1:16) = ZETAB12(1:16) .* XJAB1(1:16) .* OMEL(1:16);
        B13(1:16) = ZETAB13(1:16) .* XJAB1(1:16) .* OMEL(1:16);
        %///////////////////////////////////////////////////////////////////////
        XIB2(1:16) = (GI(1:16) - 5) ./ 6.D0;
        XJAB2(1:16) = sqrt((A .* XIB2(1:16) + B).^2 + (C .* XIB2(1:16) + D).^2) ./ 6.D0;
        %
        RGBX1(1:16) = 0.5D0 .* (XG3 - 2.D0 .* XG2 + XG1);
        RGBY1(1:16) = 0.5D0 .* (YG3 - 2.D0 .* YG2 + YG1);
        RGBX2(1:16) = (XG3 + 4.D0 .* XG2 - 5.D0 .* XG1) ./ 6.D0;
        RGBY2(1:16) = (YG3 + 4.D0 .* YG2 - 5.D0 .* YG1) ./ 6.D0;
        RGB (1:16) = sqrt((RGBX1(1:16) .* XIB2(1:16) + RGBX2(1:16)).^2 + (RGBY1(1:16) .* XIB2(1:16) + RGBY2(1:16)).^2);
        XLOB(1:16) = log(3.D0 ./ RGB(1:16));
        %
        B21(1:16) = ZETAB21(1:16) .* XJAB2(1:16) .* XLOB(1:16) .* OME(1:16);
        B22(1:16) = ZETAB22(1:16) .* XJAB2(1:16) .* XLOB(1:16) .* OME(1:16);
        B23(1:16) = ZETAB23(1:16) .* XJAB2(1:16) .* XLOB(1:16) .* OME(1:16);
        %///////////////////////////////////////////////////////////////////////
        EX(1:16) = -(XG3 - 8.D0 .* XG2 + 7.D0 .* XG1) ./ 6.D0;
        EY(1:16) = -(YG3 - 8.D0 .* YG2 + 7.D0 .* YG1) ./ 6.D0;
        XJA(1:16) = sqrt(EX(1:16).^2 + EY(1:16).^2);
        RNOR(1:16) = EY(1:16) ./ XJA(1:16);
        %///////////////////////////////////////////////////////////////////////
        S1(1:16) = A11(1:16) + A21(1:16) + B11(1:16) + B21(1:16);
        S2(1:16) = A12(1:16) + A22(1:16) + B12(1:16) + B22(1:16);
        S3(1:16) = A13(1:16) + A23(1:16) + B13(1:16) + B23(1:16);
        %XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX
        %XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX
        %-----------------------------------------------------------------------
        %            ! ξs(ソース点の位置)が0の場合
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! ξs(ソース点の位置)が0の場合に用いる内挿関数
        %
    elseif (NODO == 2)
        ZETAC1(1:16) = 0.75D0 .* GIL(1:16) .* (1.5D0 .* GIL(1:16) - 1);
        ZETAC2(1:16) = (1 - 1.5D0 .* GIL(1:16)) .* (1 + 1.5D0 .* GIL(1:16));
        ZETAC3(1:16) = 0.75D0 .* GIL(1:16) .* (1.5D0 .* GIL(1:16) + 1);
        %///////////////////////////////////////////////////////////////////////
        ZETAD1(1:16) = ZETAC3(1:16);
        ZETAD2(1:16) = ZETAC2(1:16);
        ZETAD3(1:16) = ZETAC1(1:16);
        %///////////////////////////////////////////////////////////////////////
        ZETAE1(1:16) = 0.75D0 .* GI(1:16) .* (1.5D0 .* GI(1:16) - 1);
        ZETAE2(1:16) = (1 - 1.5D0 .* GI(1:16)) .* (1 + 1.5D0 .* GI(1:16));
        ZETAE3(1:16) = 0.75D0 .* GI(1:16) .* (1.5D0 .* GI(1:16) + 1);
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[0 , +1]のとき
        %
        XIC(1:16) = GIL(1:16);
        XJAC(1:16) = sqrt((A .* XIC(1:16) + B).^2 + (C .* XIC(1:16) + D).^2);
        %
        C1(1:16) = ZETAC1(1:16) .* XJAC(1:16) .* OMEL(1:16);
        C2(1:16) = ZETAC2(1:16) .* XJAC(1:16) .* OMEL(1:16);
        C3(1:16) = ZETAC3(1:16) .* XJAC(1:16) .* OMEL(1:16);
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[-1 , 0]のとき
        %
        XID(1:16) = -GIL(1:16);
        XJAD(1:16) = sqrt((A .* XID(1:16) + B).^2 + (C .* XID(1:16) + D).^2);
        %
        D1(1:16) = ZETAD1(1:16) .* XJAD(1:16) .* OMEL(1:16);
        D2(1:16) = ZETAD2(1:16) .* XJAD(1:16) .* OMEL(1:16);
        D3(1:16) = ZETAD3(1:16) .* XJAD .* OMEL(1:16);
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[-1 , +1]のとき
        %
        XIE(1:16) = GI(1:16);
        XJAE(1:16) = sqrt((A .* XIE(1:16) + B).^2 + (C .* XIE(1:16) + D).^2);
        %
        RGEX1(1:16) = 0.5D0 .* (XG3 - 2.D0 .* XG2 + XG1);
        RGEY1(1:16) = 0.5D0 .* (YG3 - 2.D0 .* YG2 + YG1);
        RGEX2(1:16) = 0.5D0 .* (XG3 - XG1);
        RGEY2(1:16) = 0.5D0 .* (YG3 - YG1);
        RGE(1:16) = sqrt((RGEX1(1:16) .* XIE(1:16) + RGEX2(1:16)).^2 + (RGEY1(1:16) .* XIE(1:16) + RGEY2(1:16)).^2);
        XLO(1:16) = log(1.D0 ./ RGE(1:16));
        %
        E1(1:16) = ZETAE1(1:16) .* XJAE(1:16) .* XLO(1:16) .* OME(1:16);
        E2(1:16) = ZETAE2(1:16) .* XJAE(1:16) .* XLO(1:16) .* OME(1:16);
        E3(1:16) = ZETAE3(1:16) .* XJAE(1:16) .* XLO(1:16) .* OME(1:16);
        %///////////////////////////////////////////////////////////////////////
        EX(1:16) = 0.5D0 .* (XG3 - XG1);
        EY(1:16) = 0.5D0 .* (YG3 - YG1);
        XJA(1:16) = sqrt(EX(1:16).^2 + EY(1:16).^2);
        RNOR(1:16) = EY(1:16) ./ XJA(1:16);
        %///////////////////////////////////////////////////////////////////////
        S1(1:16) = C1(1:16) + D1(1:16) + E1(1:16);
        S2(1:16) = C2(1:16) + D2(1:16) + E2(1:16);
        S3(1:16) = C3(1:16) + D3(1:16) + E3(1:16);
        %XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX
        %XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX
        %-----------------------------------------------------------------------
        %            ! ξs(ソース点の位置)が+2/3の場合
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! ξs(ソース点の位置)が+2/3の場合に用いる内挿関数
        %
    elseif (NODO == 3)
        ZETAF11(1:16) = (GIL(1:16) + 2) .* GIL(1:16) ./ 8.D0;
        ZETAF12(1:16) = -(GIL(1:16) + 4) .* GIL(1:16) ./ 4.D0;
        ZETAF13(1:16) = (GIL(1:16) + 2) .* (GIL(1:16) + 4) ./ 8.D0;
        %
        ZETAF21(1:16) = (GI(1:16) + 5) .* (GI(1:16) + 1) ./ 32.D0;
        ZETAF22(1:16) = -(GI(1:16) + 1) .* (GI(1:16) + 9) ./ 16.D0;
        ZETAF23(1:16) = (GI(1:16) + 5) .* (GI(1:16) + 9) ./ 32.D0;
        %///////////////////////////////////////////////////////////////////////
        ZETAG11(1:16) = (5.D0 .* GIL(1:16) - 2) .* 5.D0 .* GIL(1:16) ./ 8.D0;
        ZETAG12(1:16) = -(5.D0 .* GIL(1:16) - 4) .* 5.D0 .* GIL(1:16) ./ 4.D0;
        ZETAG13(1:16) = (5.D0 .* GIL(1:16) - 2) .* (5.D0 .* GIL(1:16) - 4) ./ 8.D0;
        %
        ZETAG21(1:16) = (5.D0 .* GI(1:16) - 1) .* (5.D0 .* GI(1:16) - 5) ./ 32.D0;
        ZETAG22(1:16) = -(5.D0 .* GI(1:16) - 5) .* (5.D0 .* GI(1:16) + 3) ./ 16.D0;
        ZETAG23(1:16) = (5.D0 .* GI(1:16) - 1) .* (5.D0 .* GI(1:16) + 3) ./ 32.D0;
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[+2./3 , +1]のとき
        %
        XIF1(1:16) = (GIL(1:16) + 2) ./ 3.D0;
        XJAF1(1:16) = sqrt((A .* XIF1(1:16) + B).^2 + (C .* XIF1(1:16) + D).^2) ./ 3;
        %
        F11(1:16) = ZETAF11(1:16) .* XJAF1(1:16) .* OMEL(1:16);
        F12(1:16) = ZETAF12(1:16) .* XJAF1(1:16) .* OMEL(1:16);
        F13(1:16) = ZETAF13(1:16) .* XJAF1(1:16) .* OMEL(1:16);
        %///////////////////////////////////////////////////////////////////////
        XIF2(1:16) = (GI(1:16) + 5) ./ 6.D0;
        XJAF2(1:16) = sqrt((A .* XIF2(1:16) + B).^2 + (C .* XIF2(1:16) + D).^2) ./ 6.D0;

        RGFX1(1:16) = 0.5D0 .* (XG3 - 2.D0 .* XG2 + XG1);
        RGFY1(1:16) = 0.5D0 .* (YG3 - 2.D0 .* YG2 + YG1);
        RGFX2(1:16) = (5.D0 .* XG3 - 4.D0 .* XG2 - XG1) ./ 6.D0;
        RGFY2(1:16) = (5.D0 .* YG3 - 4.D0 .* YG2 - YG1) ./ 6.D0;
        RGF(1:16) = sqrt((RGFX1(1:16) .* XIF2(1:16) + RGFX2(1:16)).^2 + (RGFY1(1:16) .* XIF2(1:16) + RGFY2(1:16)).^2);
        XLOF(1:16) = log(3.D0 ./ RGF(1:16));
        %
        F21(1:16) = ZETAF21(1:16) .* XJAF2(1:16) .* XLOF(1:16) .* OME(1:16);
        F22(1:16) = ZETAF22(1:16) .* XJAF2(1:16) .* XLOF(1:16) .* OME(1:16);
        F23(1:16) = ZETAF23(1:16) .* XJAF2(1:16) .* XLOF(1:16) .* OME(1:16);
        %$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
        %            ! 積分範囲が[-1 , +2/3]のとき
        %
        XIG1(1:16) = -(5.D0 .* GIL(1:16) - 2) ./ 3.D0;
        XJAG1(1:16) = 5.D0 .* sqrt((A .* XIG1(1:16) + B).^2 + (C .* XIG1(1:16) + D).^2) ./ 3.D0;
        %
        G11(1:16) = ZETAG11(1:16) .* XJAG1(1:16) .* OMEL(1:16);
        G12(1:16) = ZETAG12(1:16) .* XJAG1(1:16) .* OMEL(1:16);
        G13(1:16) = ZETAG13(1:16) .* XJAG1(1:16) .* OMEL(1:16);
        %.///////////////////////////////////////////////////////////////////////
        XIG2(1:16) = (5.D0 .* GI(1:16) - 1) ./ 6.D0;
        XJAG2(1:16) = 5.D0 .* sqrt((A .* XIG2(1:16) + B).^2 + (C .* XIG2(1:16) + D).^2) ./ 6.D0;
        %
        RGGX1(1:16) = 0.5D0 .* (XG3 - 2.D0 .* XG2 + XG1);
        RGGY1(1:16) = 0.5D0 .* (YG3 - 2.D0 .* YG2 + YG1);
        RGGX2(1:16) = (5.D0 .* XG3 - 4.D0 .* XG2 - XG1) ./ 6.D0;
        RGGY2(1:16) = (5.D0 .* YG3 - 4.D0 .* YG2 - YG1) ./ 6.D0;
        RGG(1:16) = sqrt((RGGX1(1:16) .* XIG2(1:16) + RGGX2(1:16)).^2 + (RGGY1(1:16) .* XIG2(1:16) + RGGY2(1:16)).^2);
        XLOG(1:16) = log(0.6D0 ./ RGG(1:16));
        %
        G21(1:16) = ZETAG21(1:16) .* XJAG2(1:16) .* XLOG(1:16) .* OME(1:16);
        G22(1:16) = ZETAG22(1:16) .* XJAG2(1:16) .* XLOG(1:16) .* OME(1:16);
        G23(1:16) = ZETAG23(1:16) .* XJAG2(1:16) .* XLOG(1:16) .* OME(1:16);
        %///////////////////////////////////////////////////////////////////////
        EX(1:16) = (7.D0 .* XG3 - 8.D0 .* XG2 + XG1) ./ 6.D0;
        EY(1:16) = (7.D0 .* YG3 - 8.D0 .* YG2 + YG1) ./ 6.D0;
        XJA(1:16) = sqrt(EX(1:16).^2 + EY(1:16).^2);
        RNOR(1:16) = EY(1:16) ./ XJA(1:16);
        %///////////////////////////////////////////////////////////////////////
        S1(1:16) = F11(1:16) + F21(1:16) + G11(1:16) + G21(1:16);
        S2(1:16) = F12(1:16) + F22(1:16) + G12(1:16) + G22(1:16);
        S3(1:16) = F13(1:16) + F23(1:16) + G13(1:16) + G23(1:16);
    end

    %XXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXXX
    %-----------------------------------------------------------------------
    GOINT1(1:16) = S1(1:16) ./ PI2;
    GOINT2(1:16) = S2(1:16) ./ PI2;
    GOINT3(1:16) = S3(1:16) ./ PI2;
    GWLA(1) = GWLA(1) + sum(GOINT1(1:16));
    GWLA(2) = GWLA(2) + sum(GOINT2(1:16));
    GWLA(3) = GWLA(3) + sum(GOINT3(1:16));
    %
    HOINT1(1:16) = GOINT1(1:16) .* RNOR(1:16) ./ (XS .* 2.D0);
    HOINT2(1:16) = GOINT2(1:16) .* RNOR(1:16) ./ (XS .* 2.D0);
    HOINT3(1:16) = GOINT3(1:16) .* RNOR(1:16) ./ (XS .* 2.D0);
    HWLA(1) = HWLA(1) + sum(HOINT1(1:16));
    HWLA(2) = HWLA(2) + sum(HOINT2(1:16));
    HWLA(3) = HWLA(3) + sum(HOINT3(1:16));
